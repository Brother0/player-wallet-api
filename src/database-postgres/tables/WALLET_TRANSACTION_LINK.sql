CREATE TABLE WALLET_TRANSACTION_LINK (
    WALLET_TRANSACTION_HKEY text NOT NULL,
    WALLET_HKEY text NOT NULL,
    TRANSACTION_HKEY text NOT NULL,
    LOAD_DATE TIMESTAMP NOT NULL,
    RECORD_SOURCE VARCHAR(50) NOT NULL,

    CONSTRAINT PK_WALLET_TRANSACTION_LINK PRIMARY KEY  (
        WALLET_TRANSACTION_HKEY
    ),

    CONSTRAINT UQ_WALLET_TRANSACTION_LINK UNIQUE 
    (
        WALLET_HKEY,
        TRANSACTION_HKEY
    )

);

