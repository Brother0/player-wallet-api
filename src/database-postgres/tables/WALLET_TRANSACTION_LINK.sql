comment on column WALLET_TRANSACTION_LINK.WALLET_HKEY is 'Wallet hkey';
comment on column WALLET_TRANSACTION_LINK.TRANSACTION_HKEY is 'Transaction hkey';
comment on column WALLET_TRANSACTION_LINK.LOAD_DATE is 'Load time';
comment on column WALLET_TRANSACTION_LINK.RECORD_SOURCE is 'Origin';


